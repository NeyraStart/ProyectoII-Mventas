    private void DeleteCategoria() {
            // Evento Eliminar
        int fila = tblCategorias.getSelectedRow();
        if (fila == -1) {
            return;
        }
        Categoria categoria = new Categoria(
                Integer.parseInt(tblCategorias.getValueAt(fila, 0) + ""),
                tblCategorias.getValueAt(fila, 1) + ""
        );
        if (JOptionPane
                .showConfirmDialog(
                        this,
                        "Desea borrar el curso codigo:" + categoria.getId()) == 0) {
            cr.remove(categoria);
        }
        cargarCategorias(); 
    }

